#!/usr/bin/env bash
# setup-azure-ddns.sh — wire Metis up to Azure DNS for both metis-ddns
# and certbot's dns-azure plugin. Run once after first browser login (so
# `az login` can pop a browser auth window).
#
# Idempotent: safe to re-run any number of times. Each run:
#   - reuses the existing 'metis-ddns' app reg if found (else creates one)
#   - ensures the SP exists (no-op if already there)
#   - rotates the secret (replaces, doesn't append — old one invalidated)
#   - ensures the role assignment exists (DNS Zone Contributor on the zone)
#   - rewrites /etc/metis-ddns.env and /etc/letsencrypt/azure.ini with the
#     fresh creds
#   - restarts metis-ddns
#
# Tested 2026-04-23 against Azure CLI 2.85.0. The all-in-one
# `az ad sp create-for-rbac` is currently broken under Python 3.14
# (argparse %Y bug) — this script uses the piecemeal `ad app` /
# `ad sp` / `role assignment` commands which are unaffected.

set -euo pipefail

# ---- Locked config from project memory (project_azure_ddns.md) ----
SUBSCRIPTION_ID="ab78414a-6bf4-4d87-b27c-954c41aa8081"
RESOURCE_GROUP="rhombus"
DNS_ZONE="rhombus.rocks"
RECORD_NAME="metis"
SP_DISPLAY_NAME="metis-ddns"

ZONE_SCOPE="/subscriptions/${SUBSCRIPTION_ID}/resourceGroups/${RESOURCE_GROUP}/providers/Microsoft.Network/dnszones/${DNS_ZONE}"

# ---- Helpers ----
log()  { printf '[+] %s\n' "$*"; }
warn() { printf '[!] %s\n' "$*" >&2; }
die()  { printf '[✗] %s\n' "$*" >&2; exit 1; }

# ---- Pre-flight ----
command -v az >/dev/null || die "azure-cli not installed (sudo pacman -S azure-cli)"
command -v jq >/dev/null || die "jq not installed (sudo pacman -S jq)"

# Verify auth — prompt to login if not yet authenticated.
if ! az account show >/dev/null 2>&1; then
    log "azure-cli not authenticated; running 'az login' (browser will open)..."
    az login --use-device-code-if-no-browser >/dev/null
fi

current_sub=$(az account show --query id -o tsv)
if [[ "$current_sub" != "$SUBSCRIPTION_ID" ]]; then
    log "Switching active subscription to $SUBSCRIPTION_ID..."
    az account set --subscription "$SUBSCRIPTION_ID"
fi

TENANT_ID=$(az account show --query tenantId -o tsv)
log "Tenant: $TENANT_ID"

# Verify zone exists (cheap read; fail loudly if missing rather than create
# the SP and discover later that the role assignment scope is wrong).
if ! az network dns zone show -g "$RESOURCE_GROUP" -n "$DNS_ZONE" >/dev/null 2>&1; then
    die "DNS zone '$DNS_ZONE' not found in resource group '$RESOURCE_GROUP'"
fi

# ---- 1. App registration (idempotent: check before create) ----
APP_ID=$(az ad app list --display-name "$SP_DISPLAY_NAME" --query '[0].appId' -o tsv 2>/dev/null || true)
if [[ -z "$APP_ID" ]]; then
    log "Creating app registration '$SP_DISPLAY_NAME'..."
    APP_ID=$(az ad app create --display-name "$SP_DISPLAY_NAME" --query appId -o tsv)
else
    log "Reusing existing app '$SP_DISPLAY_NAME' (appId=$APP_ID)"
fi

# ---- 2. Service principal (idempotent: just runs cleanly if already there) ----
log "Ensuring service principal exists for app..."
az ad sp create --id "$APP_ID" >/dev/null 2>&1 || true   # already-exists returns 0 in current az

# ---- 3. Rotate credential (REPLACE behavior — old secret invalidated) ----
log "Rotating secret (default 1y; --years 2 blocked by az+py3.14 bug, will re-add when fixed)..."
cred_json=$(az ad app credential reset \
    --id "$APP_ID" \
    --display-name "${SP_DISPLAY_NAME}-secret" \
    -o json)
CLIENT_SECRET=$(echo "$cred_json" | jq -r .password)
[[ -n "$CLIENT_SECRET" && "$CLIENT_SECRET" != "null" ]] || die "credential reset returned no password"

# ---- 4. Role assignment (DNS Zone Contributor on the zone) ----
log "Ensuring DNS Zone Contributor assignment on $DNS_ZONE..."
az role assignment create \
    --assignee "$APP_ID" \
    --role "DNS Zone Contributor" \
    --scope "$ZONE_SCOPE" \
    >/dev/null 2>&1 || log "  (role assignment already exists — fine)"

# ---- 5. Write /etc/metis-ddns.env (mode 600, root-owned) ----
log "Writing /etc/metis-ddns.env..."
sudo install -d -m 755 /etc
sudo tee /etc/metis-ddns.env >/dev/null <<EOF
# Generated by setup-azure-ddns.sh on $(date -Iseconds)
# Re-run that script to rotate the secret (old one is invalidated automatically).
AZ_TENANT_ID=${TENANT_ID}
AZ_CLIENT_ID=${APP_ID}
AZ_CLIENT_SECRET=${CLIENT_SECRET}
AZ_SUBSCRIPTION_ID=${SUBSCRIPTION_ID}
AZ_RESOURCE_GROUP=${RESOURCE_GROUP}
AZ_DNS_ZONE=${DNS_ZONE}
AZ_RECORD_NAME=${RECORD_NAME}
DDNS_DISABLE_IPV4=1
EOF
sudo chmod 600 /etc/metis-ddns.env
sudo chown root:root /etc/metis-ddns.env

# ---- 6. Write /etc/letsencrypt/azure.ini for certbot dns-azure plugin ----
log "Writing /etc/letsencrypt/azure.ini..."
sudo install -d -m 755 /etc/letsencrypt
sudo tee /etc/letsencrypt/azure.ini >/dev/null <<EOF
# Generated by setup-azure-ddns.sh on $(date -Iseconds)
dns_azure_sp_client_id     = ${APP_ID}
dns_azure_sp_client_secret = ${CLIENT_SECRET}
dns_azure_tenant_id        = ${TENANT_ID}
dns_azure_environment      = AzurePublicCloud
dns_azure_zone1            = ${DNS_ZONE}:/subscriptions/${SUBSCRIPTION_ID}/resourceGroups/${RESOURCE_GROUP}
EOF
sudo chmod 600 /etc/letsencrypt/azure.ini
sudo chown root:root /etc/letsencrypt/azure.ini

# ---- 7. Kick the DDNS service ----
log "Starting metis-ddns service..."
sudo systemctl restart metis-ddns.service || warn "metis-ddns.service restart failed; check 'systemctl status metis-ddns'"
sudo systemctl enable metis-ddns.timer >/dev/null 2>&1 || true

# ---- 8. Report ----
echo
log "Azure DDNS wired. Summary:"
echo "  Tenant:      $TENANT_ID"
echo "  App / SP:    $APP_ID  ($SP_DISPLAY_NAME)"
echo "  Scope:       $DNS_ZONE  in resource group $RESOURCE_GROUP"
echo "  Records:     ${RECORD_NAME}.${DNS_ZONE} A + AAAA (next ddns run will populate)"
echo "  Secret life: 1 year (rotate by re-running this script)"
echo
echo "Next step: issue Let's Encrypt cert"
echo "  sudo certbot certonly --authenticator dns-azure \\"
echo "      --dns-azure-credentials /etc/letsencrypt/azure.ini \\"
echo "      --dns-azure-propagation-seconds 60 \\"
echo "      -d ${RECORD_NAME}.${DNS_ZONE}"
